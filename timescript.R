barplot(x12,names.arg = days, xlab = "Days", ylab = "Passengers", main = "At 12.00",col = 4)
dev.off()
png(file = "at_14.png")
barplot(x14,names.arg = days, xlab = "Days", ylab = "Passengers", main = "At 14.00",col = 5)
dev.off()
png(file = "at_16.png")
barplot(x16,names.arg = days, xlab = "Days", ylab = "Passengers", main = "At 16.00",col = 6)
dev.off()
png(file = "at_18.png")
barplot(x18,names.arg = days, xlab = "Days", ylab = "Passengers", main = "At 18.00",col = 7)
dev.off()
png(file = "at_20.png")
barplot(x20,names.arg = days, xlab = "Days", ylab = "Passengers", main = "At 20.00",col = 8)
dev.off()
png(file = "at_22.png")
barplot(x22,names.arg = days, xlab = "Days", ylab = "Passengers", main = "At 22.00",col = 9)
